# Membership operators
'membership operator is used to check the presence of anything in a sequence of data'
'simple example is ,in string we can check a specific character is present or not'
'there are 2 membership operators : in , not in '
str = 'sanjana'
print('j' in str)  # its checking if j is present in str or no
print('nan' not in str)  # its checking if 'nan' is not in str
